    private void jButton23ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton23ActionPerformed
        if (mp == null) {
            String boardCards = jTextField15.getText();
            String deadCards = jTextField21.getText();
            matchupsFinished = 0;

            String[] playerRanges = new String[]{
                jText1.getText(),
                jText2.getText(),
                jText3.getText(),
                jText4.getText(),
                jText5.getText(),
                jText6.getText(),
                jText7.getText(),
                jText8.getText(),
                jText9.getText(),
                jText10.getText()
            };

            ArrayList<String> ranges = new ArrayList<>();

            for (String range : playerRanges) {
                if (range.trim().length() == 0) {
                    break;
                }
                ranges.add(range);
            }
            if (ranges.size() == 0) {
                return;
            }
            jButton23.setText("Stop (0.00%)");

            try {
                if (!jRadioButton2.isSelected()) {
                    mp = new EnumerateMatchupCalculator(ranges.toArray(new String[ranges.size()]), boardCards, deadCards, null, this);
                }
                else {
                    mp = new MonteCarloMatchupCalculator(ranges.toArray(new String[ranges.size()]), boardCards, deadCards, null, this);
                }
                mp.start();
            }
            catch(Exception ex) {

            }
        }
        else {
            mp.stopThreads();
        }
    }//GEN-LAST:event_jButton23ActionPerformed

