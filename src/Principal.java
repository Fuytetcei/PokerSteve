    @Override
    public void matchupFinished(int count) {
        double matchupsCount = mp.getMatchupsCount();
        if (matchupsCount != -1) {
            matchupsFinished += count;
            double percentage = 100.0 * matchupsFinished / matchupsCount;
            System.out.println(percentage);
            jButton23.setText("Stop ("+String.format("%.2f",percentage)+"%)");
        }
        updateEquities();
    }

    @Override
    public void allMatchupsFinished() {
        System.out.println("Finished!");
        jButton23.setText("Evaluate");
        jTextArea1.setText(mp.getOutput());
        
        updateEquities();
        
        mp = null;
    }
    
    private void updateEquities() {
        
        JTextField[] equityFields = new JTextField[]{
            jTextField11,
            jTextField12,
            jTextField20,
            jTextField22,
            jTextField14,
            jTextField13,
            jTextField18,
            jTextField16,
            jTextField17,
            jTextField19
        };
        
        double[] equities = mp.getPlayerEquities();
        for (int i=0; i<equities.length; i++) {
            equityFields[i].setText(String.format("%.2f%%",equities[i]));
        }
        for (int i=equities.length; i<10; i++) {
            equityFields[i].setText("");
        }
    }
